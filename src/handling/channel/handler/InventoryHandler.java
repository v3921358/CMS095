    public static final void UseSummonBag(final LittleEndianAccessor slea, final MapleClient c, final MapleCharacter chr) {
        if (!chr.isAlive() || chr.hasBlockedInventory() || chr.inPVP()) {
            c.getSession().write(MaplePacketCreator.enableActions());
            c.getPlayer().marriage();
            return;
        }
        //c.getPlayer().updateTick(slea.readInt());
        slea.readInt();
        final byte slot = (byte) slea.readShort();
        final int itemId = slea.readInt();
        final Item toUse = chr.getInventory(MapleInventoryType.USE).getItem(slot);
        
        if (toUse != null && toUse.getQuantity() >= 1 && toUse.getItemId() == itemId && (c.getPlayer().getMapId() < 910000000 || c.getPlayer().getMapId() > 910000022)) {
            final Map<String, Integer> toSpawn = MapleItemInformationProvider.getInstance().getEquipStats(itemId);
            
            if (toSpawn == null) {
                c.getSession().write(MaplePacketCreator.enableActions());
                c.getPlayer().marriage();
                return;
            }
            MapleMonster ht = null;
            int type = 0;
            
            for (Entry<String, Integer> i : toSpawn.entrySet()) {
                if (i.getKey().startsWith("mob") && Randomizer.nextInt(99) <= i.getValue()) {
                    ht = MapleLifeFactory.getMonster(Integer.parseInt(i.getKey().substring(3)));
                    double range = Double.POSITIVE_INFINITY;
                    if (MapConstants.isReincarnationMob(ht.getId())) {
                        MapleMonster mob;
                        for (MapleMapObject monstermo : chr.getMap().getMapObjectsInRange(c.getPlayer().getPosition(), range, Arrays.asList(MapleMapObjectType.MONSTER))) {
                            mob = (MapleMonster) monstermo;
                            if (MapConstants.isReincarnationMob(mob.getId())) {
                                c.getPlayer().dropMessage(5, "当前地图已存在輪迴石碑，无法繼續使用。");
                                c.getSession().write(MaplePacketCreator.enableActions());
                                c.getPlayer().marriage();
                                return;
                            }
                        }
                    }
                    chr.getMap().spawnMonster_sSack(ht, chr.getPosition(), type);
                    
                    if (MapConstants.isReincarnationMob(ht.getId())) {
                        if (toUse.getItemId() != 2109020) {
                            MapleMap map = c.getPlayer().getMap();
                            MapleMonster mons = ht;
                            final java.util.Timer timer = new java.util.Timer(true);
                            TimerTask task = new TimerTask() {
                                boolean killmob = true;
                                
                                @Override
                                public void run() {
                                    if (killmob) {
                                        killmob = false;
                                    } else {
                                        killmob = true;
                                        map.killMonster(mons);
                                        timer.cancel();
                                    }
                                    
                                }
                            };
                            timer.schedule(task, 0, 600000);
                        } else {
                            chr.setReincarnation(true);
                        }
                    }
                    
                }
            }
            
            if (toUse.getItemId() == 2109020) {
                c.getSession().write(MaplePacketCreator.enableActions());
                c.getPlayer().marriage();
                return;
            }
            
            if (toUse.getItemId() == 2430158) {
                c.getSession().write(MaplePacketCreator.enableActions());
                c.getPlayer().marriage();
                return;
            }
            
            if (ht == null) {
                c.getSession().write(MaplePacketCreator.enableActions());
                c.getPlayer().marriage();
                return;
            }
            
            MapleInventoryManipulator.removeFromSlot(c, MapleInventoryType.USE, slot, (short) 1, false);
        }
        c.getSession().write(MaplePacketCreator.enableActions());
        c.getPlayer().marriage();
    }
    
    public static final void UseTreasureChest(final LittleEndianAccessor slea, final MapleClient c, final MapleCharacter chr) {
        final short slot = slea.readShort();
        final int itemid = slea.readInt();
        
        final Item toUse = chr.getInventory(MapleInventoryType.ETC).getItem((byte) slot);
        if (toUse == null || toUse.getQuantity() <= 0 || toUse.getItemId() != itemid || chr.hasBlockedInventory()) {
            c.getSession().write(MaplePacketCreator.enableActions());
            c.getPlayer().marriage();
            return;
        }
        int reward;
        int keyIDforRemoval = 0;
        String box;
        
        switch (toUse.getItemId()) {
            case 4280000: // Gold box
                reward = RandomRewards.getGoldBoxReward();
                keyIDforRemoval = 5490000;
                box = "Gold";
                break;
            case 4280001: // Silver box
                reward = RandomRewards.getSilverBoxReward();
                keyIDforRemoval = 5490001;
                box = "Silver";
                break;
            default: // Up to no good
                return;
        }

        // Get the quantity
        int amount = 1;
        switch (reward) {
            case 2000004:
                amount = 200; // Elixir
                break;
            case 2000005:
                amount = 100; // Power Elixir
                break;
        }
        if (chr.getInventory(MapleInventoryType.CASH).countById(keyIDforRemoval) > 0) {
            final Item item = MapleInventoryManipulator.addbyId_Gachapon(c, reward, (short) amount);
            
            if (item == null) {
                chr.dropMessage(5, "Please check your item inventory and see if you have a Master Key, or if the inventory is full.");
                c.getSession().write(MaplePacketCreator.enableActions());
                c.getPlayer().marriage();
                return;
            }
            MapleInventoryManipulator.removeFromSlot(c, MapleInventoryType.ETC, (byte) slot, (short) 1, true);
            MapleInventoryManipulator.removeById(c, MapleInventoryType.CASH, keyIDforRemoval, 1, true, false);
            c.getSession().write(MaplePacketCreator.getShowItemGain(reward, (short) amount, true));
            c.getSession().write(MaplePacketCreator.MonsterBox(toUse.getItemId()));
            
            if (GameConstants.gachaponRareItem(item.getItemId()) > 0) {
                World.Broadcast.broadcastSmega(MaplePacketCreator.getGachaponMega("[" + box + " Chest] " + c.getPlayer().getName(), " : Lucky winner of Gachapon!", item, (byte) 2, c.getPlayer().getClient().getChannel()));
            }
        } else {
            chr.dropMessage(5, "Please check your item inventory and see if you have a Master Key, or if the inventory is full.");
            c.getSession().write(MaplePacketCreator.enableActions());
            c.getPlayer().marriage();
        }
    }
    
    private static boolean changeFace(MapleCharacter player, int color) {
        if (player.getFace() % 1000 < 100) {
            color += player.getFace();
        } else if ((player.getFace() % 1000 >= 100) && (player.getFace() % 1000 < 200)) {
            color += player.getFace() - 100;
        } else if ((player.getFace() % 1000 >= 200) && (player.getFace() % 1000 < 300)) {
            color += player.getFace() - 200;
        } else if ((player.getFace() % 1000 >= 300) && (player.getFace() % 1000 < 400)) {
            color += player.getFace() - 300;
        } else if ((player.getFace() % 1000 >= 400) && (player.getFace() % 1000 < 500)) {
            color += player.getFace() - 400;
        } else if ((player.getFace() % 1000 >= 500) && (player.getFace() % 1000 < 600)) {
            color += player.getFace() - 500;
        } else if ((player.getFace() % 1000 >= 600) && (player.getFace() % 1000 < 700)) {
            color += player.getFace() - 600;
        } else if ((player.getFace() % 1000 >= 700) && (player.getFace() % 1000 < 800)) {
            color += player.getFace() - 700;
        }
        //if (!GameConstants.faceList.containsKey(color)) {
        //     return false;
        //}
        player.setFace(color);
        player.updateSingleStat(MapleStat.FACE, player.getFace());
        player.equipChanged();
        return true;
    }
    
